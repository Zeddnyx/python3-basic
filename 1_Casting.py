# Casting ( merubah type data ke type lain )

# casting int ke float
data_int = 8;
data_float = float (data_int)

print (" casting dari int ke flost ")
print(" hasilnya",data_float,"aslinya ",data_int)


# casting float ke int
data_float = 5.6;
data_int = int (data_float)

print("\ncasting dari float ke int")
print("hasilnya",data_int,"aslinya",data_float)


# casting str ke float
data_str = 9;
data_float = float (data_str)

print("\ncasting dari str ke float")
print("hasilnya",data_float,"aslinya",data_str)

#casting bool ke int
data_bool = True  # hanya True and False
data_int = int (data_bool)

print("\casting bool ke int")
print("hasilnya",data_int,"aslinya",data_bool)




