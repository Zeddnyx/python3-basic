# operasi aritmatika

a = 10
b = 3
hasil = a + b
print (a, '+' ,b, '=' ,hasil)

#pembagian (otomatis akan jadi float)
hasil = a / b
print (a, '/' ,b, '=' ,hasil)


#operasi exponen (pangkat) 
hasil = a ** b
print (a, '**' ,b, '=' ,hasil)


'''
operasi modulus % (sisa pembagian (jika 10 / 3 = 9 
sisa 1
1 akan jadi hasil nya
jika angka nya bulat maka akan jadi 0 (ex: 9 % 3 = 0)
'''
hasil = a % b
print (a, '%' ,b, '=' ,hasil)

 
#operasi floor division // 
#hasil akan jadi 3  karena di bulatkan  dari pembagian
hasil = a // b
print (a, '//' ,b, '=' ,hasil)
